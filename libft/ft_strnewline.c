#include "libft.h"

char *ft_strnewline(char *str)
{
	char	*temp;
	int		i;

	i = 0;
	temp = ft_strdup(str);
	while (temp[i] != '\0')
	{
		if (temp[i] == '\n')
			temp[i] = ' ';
		i++;
	}
	return (temp);
}
